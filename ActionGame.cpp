//=============================================================================
// Initializes the game
// Throws GameError on error
//=============================================================================
void ActionGame::initialize(HWND hwnd)
{
    Game::initialize(hwnd); // throws GameError

    Game::getFile()->InputFileArray2D("mapdata.txt", mapData);

    map = new Map(mapData);

    return;
}

